public class aaa {
    private int a;
    private int b;

    public aaa() {
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public String toString(int aa) {
        return "aaa{" +
                "a=" + a +
                ", b=" + b +
                ", aa= "+ aa +
                '}';
    }
}
